#include <stdio.h>

/**
 * add - adds two numbers
 * @a: num1
 * @b: num2
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: num1
 * @b: num2
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: num1
 * @b: num2
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: num1
 * @b: num2
 * Return: resu;t
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two numbers
 * @a: num1
 * @b: num2
 * Return: resu;t
 */

int mod(int a, int b)
{
	return (a % b);
}
